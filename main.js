let lines_of_code = 99;

function check_lines_of_code(){
	while(lines_of_code >= 2){
		console.log(lines_of_code + " lines of code in the file, " + lines_of_code + " lines of code; Gabe strikes one out, clears it all out...");
		lines_of_code--;
	}
	console.log(lines_of_code + " line of code in the file, " + lines_of_code + " line of code; Gabe strikes one out, clears it all out and says, \"That's all folks!\" ");
}

check_lines_of_code();



