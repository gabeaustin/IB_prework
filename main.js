let lines_of_code = 99;

function check_lines_of_code(){
	while(lines_of_code >= 2){
		document.write(lines_of_code + " lines of code in the file, " + lines_of_code + " lines of code; Gabe strikes one out, clears it all out...<br>");
		lines_of_code--;
	}
	document.write(lines_of_code + " line of code in the file, " + lines_of_code + " line of code; Gabe strikes one out, clears it all out and says, \"That's all folks!\" <br>");
}

check_lines_of_code();



